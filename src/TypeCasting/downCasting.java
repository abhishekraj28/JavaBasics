package TypeCasting;

public class downCasting {

	public static void main(String[] args) {
		
		
		/*
		Downcasting: Converting value from larger to Smaller
		Ex: long to int
	        double to float
		*/
		
		//Downcasting is a manual process
		long l=1000000;
		int n=(int)l;
		System.out.println(n);
		
		double d=100.5;
		int n1=(int)d;
		System.out.println(n1);

	}

}
