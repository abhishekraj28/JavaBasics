package TypeCasting;

public class upCasting {

	public static void main(String[] args) {
		
		/*
		Upcasting: Converting value from smaller to larger
		Ex: int to long
	        float to double
		*/
		
		//Upcasting is an automatic process of converting lower value into smaller
		int x=100;
		long l=x;
		System.out.println(l);
		
		int x1=200;
		double d=x1;
		System.out.println(d);

	}

}
